using UnityEngine;

//my singleton :)
public class MySingleton : MonoBehaviour
{
    public static MySingleton Instance { get; private set; }

    private void Awake()
    {
        if (Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }

        Instance = this;
        DontDestroyOnLoad(gameObject);
    }
}